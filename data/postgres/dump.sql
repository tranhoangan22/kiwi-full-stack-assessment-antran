-- Create tables

CREATE TABLE addresses (
    id SERIAL NOT NULL PRIMARY KEY,
    street CHARACTER VARYING(255) NOT NULL,
    postal_code CHARACTER VARYING(10) NOT NULL,
    city CHARACTER VARYING(255) NOT NULL,
    state CHARACTER VARYING(255) NOT NULL,
    country_code CHARACTER VARYING(2) NOT NULL,
    geolocation POINT NOT NULL
);
ALTER TABLE addresses OWNER TO kiwi;

CREATE TABLE doors (
    id SERIAL NOT NULL PRIMARY KEY,
    sensor_uuid CHARACTER VARYING(64) NOT NULL,
    name CHARACTER VARYING(255) NOT NULL,
    address_id INTEGER NOT NULL REFERENCES addresses(id),
    installation_time TIMESTAMPTZ NOT NULL DEFAULT now()
);
ALTER TABLE doors OWNER TO kiwi;

CREATE TABLE users (
    id SERIAL NOT NULL PRIMARY KEY,
    email CHARACTER VARYING(255) NOT NULL,
    first_name CHARACTER VARYING(255) NOT NULL,
    last_name CHARACTER VARYING(255) NOT NULL,
    creation_time TIMESTAMPTZ NOT NULL DEFAULT now()
);
ALTER TABLE users OWNER TO kiwi;

CREATE TABLE user_door_permissions (
    id SERIAL NOT NULL PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id),
    door_id INTEGER NOT NULL REFERENCES doors(id),
    creation_time TIMESTAMPTZ NOT NULL DEFAULT now()
);
ALTER TABLE user_door_permissions OWNER TO kiwi;


